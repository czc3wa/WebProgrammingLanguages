<!-- PHP programs have access to predefined variables -->
<!-- These variables allow the script access to server information,  -->
<!-- form parameters, environment information, etc. -->
<!-- They are helpful for determining and maintaining state information. -->

<!-- $_SERVER is an array containing much information about the server -->
<!-- $_POST is an array containing variables passed to a script via HTTP POST -->
<!-- $_GET is an array of variables passed to the current script via the URL parameters -->
<!-- $_COOKIE is an array containing cookies -->

  <?php
    # Here are a few PHP predefined variables
    echo $_SERVER["HTTP_HOST"], "<br/>";
    echo $_SERVER["SERVER_SOFTWARE"], "<br/>";
    echo $_SERVER["SCRIPT_FILENAME"], "<br/>";
    echo $_POST, "<br/>";

    echo "<br />";
    
    # The output from the loop below shows all of the $_SERVER
    # values for this execution. 
    # Note: we will discuss how the loop works shortly (after discussing arrays).  
    
    # Notice that this script has not POST parameters. 
    # If $_POST is used in the loop, the output will be empty. 
    # When we process forms, POST will not be empty and we will extract values from it.
    # (just like our formHandler.php example)
    echo "Values in the \$_SERVER Variable: <br/>";
    foreach ($_SERVER as $key => $val)
    {
	  echo "$key has value $val <br/>";
    }
 
    # The phpinfo() function is very useful, showing the configuration
    # information about the server.
    phpinfo();
  ?>
